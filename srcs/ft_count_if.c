#include "libft.h"

int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int count;

	i = -1;
	count = 0;
	while (tab[++i] != 0)
	{
		if (f(tab[i]) == 1)
			count++;
	}
	return (count);
}
